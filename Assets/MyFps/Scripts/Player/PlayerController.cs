using UnityEngine;
using UnityEngine.InputSystem;

namespace MyFps
{
    public class PlayerController : MonoBehaviour
    {
        #region Variables
        // 참조
        private CharacterController controller;
        public PistolShoot pistolShoot;

        // 입력
        private Vector2 inputMove;

        // 이동
        [SerializeField]
        private float moveSpeed = 10f;

        // 중력
        private float gravity = -9.81f;
        [SerializeField]
        private Vector3 velocity;       // 중력 계산에 의한 이동 속도

        // 그라운드 체크
        public Transform groundCheck;   // 발바닥 위치
        [SerializeField] private float checkRange = 0.2f;       // 체크하는 구의 반경
        [SerializeField] private LayerMask groundMask;          // 그라운드 레이어 판별

        // 점프 높이
        [SerializeField]
        private float jumpHeight = 5f;
        #endregion

        #region Unity Event Method
        private void Start()
        {
            // 참조
            controller = this.GetComponent<CharacterController>();
        }

        private void Update()
        {
            // 땅에 있으면
            bool isGrounded = GroundCheck();
            if (isGrounded && velocity.y < 0f)
            {
                velocity.y = -10f;
            }

            // 방향
            // Global 축 이동
            // Vector3 moveDir = Vector3.right * inputMove.x + Vector3.forward * inputMove.y;

            // local 축 이동
            Vector3 moveDir = transform.right * inputMove.x + transform.forward * inputMove.y;

            // 이동
            controller.Move(moveDir * Time.deltaTime * moveSpeed);

            // 중력에 따른 y축 이동
            velocity.y += gravity * Time.deltaTime;
            controller.Move(velocity * Time.deltaTime);
        }
        #endregion

        #region Custom Method
        // Input 시스템에 등록할 함수
        public void OnMove(InputAction.CallbackContext context)
        {
            inputMove = context.ReadValue<Vector2>();
        }

        public void OnJump(InputAction.CallbackContext context)
        {
            if (context.started && GroundCheck())
            {
                // 점프 높이만큼 뛰기 위한 속도 구하기
                velocity.y = Mathf.Sqrt(-2f * gravity * jumpHeight);
            }
        }

        public void OnFire(InputAction.CallbackContext context)
        {
            // 무장 체크
            if (PlayerDataManager.Instance.Weapon == WeaponType.None)
            {
                Debug.Log("Weapon Type : None");
                return;
            }


            if (context.started) // key down, button down
            { 
                pistolShoot.Fire();
            }
        }

        // 그라운드 체크
        bool GroundCheck()
        {
            return Physics.CheckSphere(groundCheck.position, checkRange, groundMask);
        }
        #endregion
    }
}

